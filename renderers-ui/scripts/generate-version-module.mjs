import fs from 'node:fs'
import path from 'node:path'
import { fileURLToPath } from 'node:url'

const __dirname = path.dirname(fileURLToPath(import.meta.url))
const rootDir = path.resolve(__dirname, '..')
const packageJsonPath = path.join(rootDir, 'package.json')
const outputDir = path.join(rootDir, 'src', 'generated')
const outputPath = path.join(outputDir, 'version.ts')

const packageJson = JSON.parse(fs.readFileSync(packageJsonPath, 'utf8'))
const version = packageJson.version

const content = `// Generated by scripts/generate-version-module.mjs\nexport const ANALYSIS_RENDERERS_VERSION = ${JSON.stringify(version)} as const\n`

if (process.argv.includes('--check')) {
  const existing = fs.existsSync(outputPath) ? fs.readFileSync(outputPath, 'utf8') : null

  if (existing !== content) {
    console.error(`Generated version module is stale: expected ${outputPath} to match package.json version ${version}`)
    process.exit(1)
  }

  process.exit(0)
}

fs.mkdirSync(outputDir, { recursive: true })

const existing = fs.existsSync(outputPath) ? fs.readFileSync(outputPath, 'utf8') : null
if (existing !== content) {
  fs.writeFileSync(outputPath, content)
}
